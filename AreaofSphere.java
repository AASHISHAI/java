import java.util.*;

public class AreaofSphere {
    public static void main(String args[]){
        Scanner sc=new Scanner(System.in);
        System.out.println("Enter the value of r :");
        float r=sc.nextInt();
        double area=((4*3.14*r*r));
        System.out.println("The area of Sphere is :"+area);
    }
    
}
